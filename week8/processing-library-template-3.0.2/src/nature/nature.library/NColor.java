package template.library;

public class NColor {

	public int r;
	public int g;
	public int b;
	public int a;
	 
	 NColor(int r,int g, int b){
	    this.r = r;
	    this.g = g;
	    this.b = b;
	    this.a = 255;
	 }
	 
	 NColor(int r,int g, int b, int a){
		    this.r = r;
		    this.g = g;
		    this.b = b;
		    this.a = a;
	 }
	

}
